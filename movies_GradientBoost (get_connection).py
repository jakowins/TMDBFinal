    "#cargar datos \n",
    "\n",
    "QUERY = \"\"\"\n",
    "SELECT\n",
    "    m.movie_id,\n",
    "    m.budget,\n",
    "    m.revenue,\n",
    "    m.runtime,\n",
    "    m.popularity,\n",
    "    m.vote_count,\n",
    "    EXTRACT(YEAR FROM m.release_date) AS release_year,\n",
    "    m.adult,\n",
    "    m.video,\n",
    "    m.title,\n",
    "    m.overview,\n",
    "    m.tagline,\n",
    "    g.name AS genre_name\n",
    "FROM movies m\n",
    "LEFT JOIN movie_genres mg ON m.movie_id = mg.movie_id\n",
    "LEFT JOIN genres g ON mg.genre_id = g.genre_id\n",
    "WHERE m.budget > 0 AND m.revenue > 0;\n",
    "\"\"\"\n",
    "df_raw = pd.read_sql(QUERY, engine)\n",
    "\n",
    "\n",
    "#target\n",
    "df_raw['success'] = (df_raw['revenue'] > df_raw['budget'] * 2).astype(int)\n",
    "\n",
    "#features\n",
    "df_raw['adult'] = df_raw['adult'].astype(int)\n",
    "df_raw['video'] = df_raw['video'].astype(int)\n",
    "numeric_features = ['budget', 'runtime', 'popularity', 'vote_count', 'release_year', 'adult', 'video']\n",
    "\n",
    "df_movies_unique = df_raw.drop_duplicates(subset='movie_id').set_index('movie_id')\n",
    "X_num = df_movies_unique[numeric_features].fillna(df_movies_unique[numeric_features].median())\n",
    "\n",
    "#one-hot\n",
    "genre_ohe = df_raw[['movie_id', 'genre_name']].dropna()\n",
    "genre_ohe['value'] = 1\n",
    "genres_df = genre_ohe.pivot_table(\n",
    "    index='movie_id',\n",
    "    columns='genre_name',\n",
    "    values='value',\n",
    "    aggfunc='max',\n",
    "    fill_value=0\n",
    ")\n",
    "genres_df = genres_df.reindex(df_movies_unique.index, fill_value=0)\n",
    "genre_cols = genres_df.columns\n",
    "\n",
    "\n",
    "#texto\n",
    "df_movies_unique['text_combined'] = (\n",
    "    df_movies_unique['title'].fillna(\"\") + \" \" +\n",
    "    df_movies_unique['overview'].fillna(\"\") + \" \" +\n",
    "    df_movies_unique['tagline'].fillna(\"\")\n",
    ")\n",
    "\n",
    "tfidf = TfidfVectorizer(max_features=256, stop_words='english') \n",
    "X_tfidf = tfidf.fit_transform(df_movies_unique['text_combined']).toarray()\n",
    "\n",
    "pca = PCA(n_components=64, random_state=42)  \n",
    "X_text = pca.fit_transform(X_tfidf).astype('float32')\n",
    "\n",
    "\n",
    "# Dataset final\n",
    "X = np.hstack([X_num.values, genres_df.values, X_text])\n",
    "y = df_movies_unique['success'].values\n",
    "\n",
    "\n",
    "# train/test\n",
    "X_train, X_test, y_train, y_test = train_test_split(\n",
    "    X, y, test_size=0.2, stratify=y, random_state=42\n",
    ")\n",
    "\n",
    "\n",
    "# Escalado\n",
    "scaler = MinMaxScaler()\n",
    "X_train_scaled = scaler.fit_transform(X_train)\n",
    "X_test_scaled = scaler.transform(X_test)\n",
    "\n",
    "\n",
    "# Modelo\n",
    "gb = GradientBoostingClassifier(\n",
    "    n_estimators=400,\n",
    "    learning_rate=0.05,\n",
    "    max_depth=3,\n",
    "    random_state=42\n",
    ")\n",
    "gb.fit(X_train_scaled, y_train)\n",
    "\n",
    "\n",
    "#Comprobaci√≥n\n",
    "y_pred = gb.predict(X_test_scaled)\n",
